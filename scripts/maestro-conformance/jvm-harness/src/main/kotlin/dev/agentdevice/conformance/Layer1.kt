package dev.agentdevice.conformance

import com.fasterxml.jackson.databind.node.ArrayNode
import com.fasterxml.jackson.databind.node.ObjectNode
import maestro.orchestra.yaml.YamlCommandReader
import java.nio.file.Path

/**
 * Layer 1 — parser normalization generated from upstream.
 *
 * Drives the pinned `YamlCommandReader` over every corpus flow and captures,
 * per flow, either the faithful parsed command list or the upstream rejection
 * (exception class + message). The Node verifier compares our engine's parse of
 * the same flow against this generated capture and classifies each flow as
 * parses-identically / explicitly-rejected / MISMATCH.
 */
fun emitLayer1(corpusDir: Path, flows: List<CorpusFlow>): ObjectNode {
    val root = fixtureMapper.createObjectNode()
    root.put("layer", 1)
    root.put("description", "Upstream Maestro parser capture. Generated by the JVM harness; do not hand-edit.")
    val flowsNode: ArrayNode = root.putArray("flows")

    for (flow in flows) {
        val node = flowsNode.addObject()
        node.put("id", flow.id)
        node.put("file", flow.file)
        val path = corpusDir.resolve(flow.file)
        try {
            val commands = YamlCommandReader.readCommands(path)
            node.put("status", "parsed")
            val commandsNode = node.putArray("commands")
            for (command in commands) {
                val active = activeCommand(command)
                    ?: error("MaestroCommand with no active sub-command in ${flow.file}")
                val entry = commandsNode.addObject()
                entry.put("type", active.javaClass.simpleName)
                entry.set<ObjectNode>("fields", fixtureMapper.valueToTree(active))
            }
        } catch (t: Throwable) {
            node.put("status", "rejected")
            val error = node.putObject("error")
            error.put("class", rootCause(t).javaClass.simpleName)
            error.put("message", (rootCause(t).message ?: "").lineSequence().first().trim())
        }
    }
    return root
}

private fun rootCause(t: Throwable): Throwable {
    var current = t
    while (current.cause != null && current.cause !== current) current = current.cause!!
    return current
}
